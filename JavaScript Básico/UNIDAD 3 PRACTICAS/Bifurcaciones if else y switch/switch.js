// Switch
let nota = 5;

switch (nota){
    case 5:
        console.log("Buen trabajo, sobresaliente");
        break;
    case 4:
        console.log("Buen trabajo, arriba del promedio");
        break;
    case 3:
        console.log("Buen trabajo, ptomrfio");
        break;
    case 2:
        console.log("Perdiste, pero echale ganas");
        break;
    case 1:
        console.log("Perdiste por no estudiar");
        break;
    default:
        console.log("Ingresa una nota entre el 1 y el 5")
        break;
}