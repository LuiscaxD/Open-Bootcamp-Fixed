// If else + else if
let nota = 100;

if(nota === 5){
    console.log("Usted aprobó con nota sobresaliente");
}else if(nota === 4){
    console.log("Usted aprobó, pero podrias haberlo hecho mejor");
}else if(nota === 3){
    console.log("Usted aprobó pero a miuy duras penas");
}else if(nota === 2){
    console.log("Usted reprobó, pero esfuerzate un poco mas y lo lograrás");
}else if(nota === 1){
    console.log("Usted reprobó, suerte para la próxima");
}else{
    console.log("Error, introduce una nota valida entre el 1 y el 5")
}